topside = float(input("Enter the length of the top side of the trapezoid: "))
bottomside = float(input("Enter the length of the bottom side of the trapezoid: "))
height = float(input("Enter the height of the trapezoid: "))

area = 0.5 * (topside + bottomside) * height

print("The area of your trapezoid is: {:.2f} units squared".format(area))

